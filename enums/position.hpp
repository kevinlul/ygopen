#define POS_ATTACK           (POS_FACEUP_ATTACK + POS_FACEDOWN_ATTACK)
#define POS_DEFENSE          (POS_FACEUP_DEFENSE + POS_FACEDOWN_DEFENSE)

#endif // ENUM_POSITION_HPP
